// Делим на блоки по 25
const chunkArray = <T>(arr: T[], size: number): T[][] =>
    Array.from({ length: Math.ceil(arr.length / size) }, (_, i) =>
        arr.slice(i * size, i * size + size)
    );

const allAdverbsArray = Object.values(adverbs);

const adverbChunks = chunkArray(allAdverbsArray, 25);

// Создаём объект с блоками: adverbs_1, adverbs_2, ...
const newAdverbs: { [key: string]: { [key: number]: DictItem } } = {};
adverbChunks.forEach((chunk, index) => {
    const blockKey = `adverbs_${index + 1}`;
    const block: { [key: number]: DictItem } = {};
    chunk.forEach((item, i) => {
        block[i + 1] = item;
    });
    newAdverbs[blockKey] = block;
});

export { newAdverbs };
